<script>
function downloadPlugin(type) {
    // Show loading state
    event.target.innerHTML = '<i class="fas fa-spinner fa-spin mr-2"></i> Preparing...';
    event.target.disabled = true;
    
    // Simulate plugin generation
    setTimeout(() => {
        // Create download link
        const link = document.createElement('a');
        link.href = `/api/plugins/download/${type}`;
        link.download = `culturaltranslate-${type}-plugin.zip`;
        document.body.appendChild(link);
        link.click();
        document.body.removeChild(link);
        
        // Reset button
        event.target.innerHTML = '<i class="fas fa-download mr-2"></i> Download Plugin';
        event.target.disabled = false;
        
        // Show success message
        alert(`${type.charAt(0).toUpperCase() + type.slice(1)} plugin download started!`);
    }, 1000);
}
</script>
